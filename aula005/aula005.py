'''
+,-,*,/ , //, **, %, ()
// - divisao inteiro (resultado arredondado)
** - potenciacao
%  - retorna o resto da divisao
() - alteracao a precedencia nos calculos
'''

# * pode ser usado para repetir string
print(2 * 'Alan')
# + pode user usado para concatenar strings
print('Alan' + ' ' + 'Coimbra')
print('Ele tem' + ' ' + str(32) + ' ' + 'anos')  # tratamento do int para string

# Divisao arredondando
print(10 // 3)

# Potenciacao
print(2**10)

# Modulo (resto) da divisao
print(10 % 3)

